function findTopicKey(topic: string): string | null {
  const lower = topic.toLowerCase().trim();
  const keys = Object.keys(topicContent);
  for (const key of keys) {
    if (lower.includes(key) || key.includes(lower)) {
      return key;
    }
  }
  return null;
}

export function generateCourseContent(topic: string): CourseLevel[] {
  const topicKey = findTopicKey(topic);

  const lessons = topicKey
    ? {
        basic: topicContent[topicKey].basic,
        medium: topicContent[topicKey].medium,
        advanced: topicContent[topicKey].advanced,
      }
    : {
        basic: fallbackContent.basic,
        medium: fallbackContent.medium,
        advanced: fallbackContent.advanced,
      };

  return [
    {
      title: `${topic} - Fundamentals`,
      description: `Master the fundamentals of ${topic} from scratch. Step-by-step lessons designed for absolute beginners.`,
      difficulty: "beginner",
      lessons: lessons.basic,
    },
    {
      title: `${topic} - Intermediate`,
      description: `Level up your ${topic} skills with real-world patterns, best practices, and professional techniques.`,
      difficulty: "intermediate",
      lessons: lessons.medium,
    },
    {
      title: `${topic} - Advanced & Production`,
      description: `Master advanced ${topic} concepts, architecture, and production-ready patterns used by senior developers.`,
      difficulty: "advanced",
      lessons: lessons.advanced,
    },
  ];
}
